#include <stdio.h>
int main(void)
{
    int number = 5;
    float floating_point = 3.14;
    char letter = 'A';
    char string[] = "im here";

    printf("This is int number: %d\n", number);
    printf("This is float number: %.2f\n", floating_point);
    printf("This is letter: %c\n", letter);
    printf("This is string: %s\n", string);

    
    
    return (0);
}